async function outputs() {
  const product = validateProduct(process.env.CHIMERA_PRODUCT_JSON ? JSON.parse(process.env.CHIMERA_PRODUCT_JSON) : JSON.parse(await readFile(productPath, 'utf8')));
  const logo = await readFile(logoPath, 'utf8');
  if (!logo.includes('viewBox="0 0 512 512"') || /\b(?:width|height)=|<(?:image|use)|gradient|font-/i.test(logo)) {
    fail('logo.svg', 'must be self-contained, scalable, and use solid geometry');
  }
  const mark = logo.match(/<g id="icon"[^>]*>([\s\S]*?)<\/g>/)?.[1]?.trim();
  if (!mark) fail('logo.svg', 'must contain icon geometry');
  const png = async (svg, width, height) => sharp(svg, { density: 144 }).resize(width, height, { fit: 'fill' }).ensureAlpha().png({ compressionLevel: 9, adaptiveFiltering: false }).toBuffer();
  return new Map([
    [generatedPath, Buffer.from(generateProductModule(product))],
    [generatedConfigPath, Buffer.from(generateProductConfigModule(product))],
    [path.join(imagesPath, 'icon.png'), await png(iconSvg(mark, '#FFFFFF'), 1024, 1024)],
    [path.join(imagesPath, 'icon-adaptive.png'), await png(iconSvg(mark, 'none', '#FFFFFF'), 1024, 1024)],
    [path.join(imagesPath, 'icon-monochrome.png'), await png(iconSvg(mark, 'none', '#FFFFFF'), 1024, 1024)],
    [path.join(imagesPath, 'splash-android-light.png'), await png(iconSvg(mark, '#FFFFFF'), 1024, 1024)],
    [path.join(imagesPath, 'splash-android-dark.png'), await png(iconSvg(mark, '#000000', '#FFFFFF'), 1024, 1024)],
    [path.join(imagesPath, 'favicon.png'), await png(iconSvg(mark, 'none'), 1024, 1024)],
    [path.join(imagesPath, 'logotype-light.png'), await png(wordmarkSvg(mark, '#FFFFFF'), 1965, 523)],
    [path.join(imagesPath, 'logotype-dark.png'), await png(wordmarkSvg(mark, '#000000'), 1965, 523)],
  ]);
}

export async function writeOutputsAtomically(rendered, operations = { rename, unlink, writeFile }) {
  const staged = [];
  try {
    for (const [file, contents] of rendered) {
      const temporary = path.join(path.dirname(file), `.${path.basename(file)}.${randomUUID()}.tmp`);
      const backup = path.join(path.dirname(file), `.${path.basename(file)}.${randomUUID()}.backup`);
      staged.push({ temporary, file, backup, backedUp: false, installed: false });
      await operations.writeFile(temporary, contents);
    }
    try {
      for (const entry of staged) {
        try {
          await operations.rename(entry.file, entry.backup);
          entry.backedUp = true;
        } catch (error) {
          if (error?.code !== 'ENOENT') throw error;
        }
        await operations.rename(entry.temporary, entry.file);
        entry.installed = true;
      }
    } catch (commitError) {
      const rollbackErrors = [];
      for (const entry of staged.toReversed()) {
        if (entry.installed) {
          try { await operations.unlink(entry.file); } catch (error) { if (error?.code !== 'ENOENT') rollbackErrors.push(error); }
        }
        if (entry.backedUp) {
          try { await operations.rename(entry.backup, entry.file); } catch (error) { rollbackErrors.push(error); }
        }
      }
      if (rollbackErrors.length) throw new AggregateError([commitError, ...rollbackErrors], 'Asset commit failed and rollback was incomplete');
      throw commitError;
    }
  } finally {
    await Promise.all(staged.flatMap((entry) => [entry.temporary, entry.backup].map((file) => operations.unlink(file).catch(() => {}))));
  }
}

async function main(check) {
  let stale = false;
  const rendered = await outputs();
  for (const [file, expected] of rendered) {
    if (check) {
      const actual = await readFile(file).catch(() => null);
      if (!actual?.equals(expected)) { console.error(`Out of date: ${path.relative(root, file)}`); stale = true; }
    } else {
      await mkdir(path.dirname(file), { recursive: true });
    }
  }
  if (!check) await writeOutputsAtomically(rendered);
  if (stale) process.exitCode = 1;
}

if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  const args = process.argv.slice(2);
  if (args.some((arg) => arg !== '--check') || args.filter((arg) => arg === '--check').length > 1) {
    console.error('Usage: node scripts/generate-chimera-brand.mjs [--check]'); process.exitCode = 2;
  } else {
    await main(args.includes('--check'));
  }
}
