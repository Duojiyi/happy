import { pathToFileURL } from 'node:url';
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import sharp from 'sharp';

const root = path.resolve(import.meta.dirname, '..');
const productPath = path.join(root, 'brand/chimera/product.json');
const logoPath = path.join(root, 'brand/chimera/logo.svg');
const generatedPath = path.join(root, 'packages/happy-app/sources/chimera/product.generated.ts');
const imagesPath = path.join(root, 'packages/happy-app/sources/assets/images');
const keys = [
  'androidApplicationId', 'androidSignerSha256', 'androidVersionCode', 'chimeraRevision',
  'deepLinkSchemes', 'productName', 'relayOrigin', 'repository', 'slug',
  'updatePublicKey', 'upstreamAppVersion',
];
const fixed = {
  productName: 'Chimera', slug: 'chimera', androidApplicationId: 'org.chimerahub.chimera',
  repository: 'Duojiyi/happy', relayOrigin: 'https://39.98.68.173',
};

function fail(field, detail) {
  throw new Error(`Invalid ${field}: ${detail}`);
}

export function validateProduct(input) {
  if (!input || typeof input !== 'object' || Array.isArray(input)) fail('product', 'must be an object');
  const actualKeys = Object.keys(input).sort();
  const expectedKeys = [...keys].sort();
  if (JSON.stringify(actualKeys) !== JSON.stringify(expectedKeys)) {
    fail('product keys', `expected exactly ${expectedKeys.join(', ')}`);
  }
  for (const [field, expected] of Object.entries(fixed)) {
    if (input[field] !== expected) fail(field, `must be ${expected}`);
  }
  if (JSON.stringify(input.deepLinkSchemes) !== '["chimera","happy"]') {
    fail('deepLinkSchemes', 'must be exactly [chimera, happy]');
  }
  let relay;
  try { relay = new URL(input.relayOrigin); } catch { fail('relayOrigin', 'must be a URL'); }
  if (relay.protocol !== 'https:' || relay.hostname !== '39.98.68.173' || relay.origin !== input.relayOrigin) {
    fail('relayOrigin', 'must be the fixed HTTPS literal-IP origin');
  }
  if (typeof input.upstreamAppVersion !== 'string' || !/^(0|[1-9]\d*)\.(0|[1-9]\d*)\.(0|[1-9]\d*)(?:-[0-9A-Za-z-]+(?:\.[0-9A-Za-z-]+)*)?(?:\+[0-9A-Za-z-]+(?:\.[0-9A-Za-z-]+)*)?$/.test(input.upstreamAppVersion)) {
    fail('upstreamAppVersion', 'must be SemVer');
  }
  for (const field of ['chimeraRevision', 'androidVersionCode']) {
    if (!Number.isSafeInteger(input[field]) || input[field] <= 0) fail(field, 'must be a positive integer');
  }
  if (typeof input.updatePublicKey !== 'string' || !/^[A-Za-z0-9_-]{43}$/.test(input.updatePublicKey)) {
    fail('updatePublicKey', 'must be 43-character base64url');
  }
  const publicKey = Buffer.from(input.updatePublicKey, 'base64url');
  if (publicKey.length !== 32 || publicKey.toString('base64url') !== input.updatePublicKey) {
    fail('updatePublicKey', 'must encode exactly 32 Ed25519 public-key bytes');
  }
  if (typeof input.androidSignerSha256 !== 'string' || !/^[0-9A-F]{64}$/.test(input.androidSignerSha256)) {
    fail('androidSignerSha256', 'must be 64 uppercase hexadecimal characters');
  }
  return Object.freeze({ ...input, deepLinkSchemes: Object.freeze([...input.deepLinkSchemes]) });
}

const constantNames = {
  androidApplicationId: 'ANDROID_APPLICATION_ID', androidSignerSha256: 'ANDROID_SIGNER_SHA256',
  androidVersionCode: 'ANDROID_VERSION_CODE', chimeraRevision: 'CHIMERA_REVISION',
  deepLinkSchemes: 'DEEP_LINK_SCHEMES', productName: 'PRODUCT_NAME', relayOrigin: 'RELAY_ORIGIN',
  repository: 'REPOSITORY', slug: 'PRODUCT_SLUG', updatePublicKey: 'UPDATE_PUBLIC_KEY',
  upstreamAppVersion: 'UPSTREAM_APP_VERSION',
};

export function generateProductModule(product) {
  const lines = Object.entries(constantNames)
    .sort(([, a], [, b]) => a.localeCompare(b))
    .map(([field, name]) => `export const ${name} = ${JSON.stringify(product[field])} as const;`);
  return `// Generated by scripts/generate-chimera-brand.mjs. Do not edit.\n${lines.join('\n')}\n`;
}

const iconSvg = (logo, background, foreground = '#000000') => Buffer.from(
  `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 1024 1024"><rect width="1024" height="1024" fill="${background}"/><g transform="translate(128 128) scale(1.5)" fill="${foreground}">${logo}</g></svg>`,
);
const markPaths = '<path d="M80 72h352v88H168v192h264v88H80z"/>';
const wordmarkSvg = (background, foreground) => Buffer.from(`<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 1965 523"><rect width="1965" height="523" fill="${background}"/><g fill="${foreground}"><g transform="translate(45 5)">${markPaths}</g><path d="M610 112h280v64H682v171h208v64H610zm325 0h72v110h116V112h72v299h-72V286h-116v125h-72zm313 0h72v299h-72zm125 0h78l76 164 76-164h78v299h-70V236l-58 125h-52l-58-125v175h-70zm361 0h186v64h-114v53h99v62h-99v56h114v64h-186z"/></g></svg>`);

async function outputs() {
  const product = validateProduct(JSON.parse(await readFile(productPath, 'utf8')));
  const logo = await readFile(logoPath, 'utf8');
  if (!logo.includes('viewBox="0 0 512 512"') || /\b(?:width|height)=|<(?:image|use)|gradient|font-/i.test(logo)) {
    fail('logo.svg', 'must be self-contained, scalable, and use solid geometry');
  }
  const png = async (svg, width, height) => sharp(svg, { density: 144 }).resize(width, height, { fit: 'fill' }).ensureAlpha().png({ compressionLevel: 9, adaptiveFiltering: false }).toBuffer();
  return new Map([
    [generatedPath, Buffer.from(generateProductModule(product))],
    [path.join(imagesPath, 'icon.png'), await png(iconSvg(markPaths, '#FFFFFF'), 1024, 1024)],
    [path.join(imagesPath, 'icon-adaptive.png'), await png(iconSvg(markPaths, 'none'), 1024, 1024)],
    [path.join(imagesPath, 'icon-monochrome.png'), await png(iconSvg(markPaths, 'none'), 1024, 1024)],
    [path.join(imagesPath, 'splash-android-light.png'), await png(iconSvg(markPaths, '#FFFFFF'), 1024, 1024)],
    [path.join(imagesPath, 'splash-android-dark.png'), await png(iconSvg(markPaths, '#000000', '#FFFFFF'), 1024, 1024)],
    [path.join(imagesPath, 'favicon.png'), await png(iconSvg(markPaths, 'none'), 1024, 1024)],
    [path.join(imagesPath, 'logotype-light.png'), await png(wordmarkSvg('#FFFFFF', '#000000'), 1965, 523)],
    [path.join(imagesPath, 'logotype-dark.png'), await png(wordmarkSvg('#000000', '#FFFFFF'), 1965, 523)],
  ]);
}

async function main(check) {
  let stale = false;
  for (const [file, expected] of await outputs()) {
    if (check) {
      const actual = await readFile(file).catch(() => null);
      if (!actual?.equals(expected)) { console.error(`Out of date: ${path.relative(root, file)}`); stale = true; }
    } else {
      await mkdir(path.dirname(file), { recursive: true });
      await writeFile(file, expected);
    }
  }
  if (stale) process.exitCode = 1;
}

if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  const args = process.argv.slice(2);
  if (args.some((arg) => arg !== '--check') || args.filter((arg) => arg === '--check').length > 1) {
    console.error('Usage: node scripts/generate-chimera-brand.mjs [--check]'); process.exitCode = 2;
  } else {
    await main(args.includes('--check'));
  }
}
